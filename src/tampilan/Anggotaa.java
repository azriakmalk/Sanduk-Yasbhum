    public void datatable(){
       Object[] Baris ={"NRP","Nama","Pangkat","Kesatuan"};
        tabmode = new DefaultTableModel(null,Baris);
        tabel_anggota.setModel(tabmode);
       
        String sql = "select anggota.nrp,anggota.nama,kesatuan.kesatuan,pangkat.pangkat from anggota,kesatuan,pangkat where pangkat.id_pangkat=anggota.id_pangkat and kesatuan.id_kesatuan=anggota.id_kesatuan";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);            
            while(hasil.next()){               
                String a = hasil.getString("anggota.nrp");
                String b = hasil.getString("anggota.nama");
                String c = hasil.getString("pangkat.pangkat");
                String d = hasil.getString("kesatuan.kesatuan");
                
                String[] data={a,b,c,d};
                tabmode.addRow(data);
            }
        }catch (Exception e){
            
        }
    }
    
    
    
    
