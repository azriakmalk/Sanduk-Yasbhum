    private void btn_konfirmasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_konfirmasiActionPerformed
        // TODO add your handling code here:
        if(tnrp.getText().equals("")){
            JOptionPane.showMessageDialog(null,"NRP Tidak Boleh Kosong","Peringatan",0);
        }else if(tnama.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Nama Anggota Tidak Boleh Kosong","Peringatan",0);
        }else if(cpangkat.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Harap pilih Pangkat terlebih dahulu","Peringatan",0);
        }else if(ckesatuan.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Harap pilih Kesatuan terlebih dahulu","Peringatan",0);
        }else{
            String sql="insert into anggota values(?,?,?,?)";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, tnrp.getText());
                stat.setString(2, tnama.getText());
                stat.setString(3, lpangkat.getText());
                stat.setString(4, lkesatuan.getText());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
                datatable();

                bersih();

            }catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan : ID Pangkat sudah ada \n"+e);

            }
        }
    }//GEN-LAST:event_btn_konfirmasiActionPerformed

