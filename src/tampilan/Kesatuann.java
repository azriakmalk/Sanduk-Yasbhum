    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(tid.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Id Kesatuan Tidak Boleh Kosong","Peringatan",0);
        }else if(tkesatuan.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Nama Kesatuan Tidak Boleh Kosong","Peringatan",0);
        }else{
            String sql="insert into kesatuan values(?,?,?)";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, tid.getText());
                stat.setString(2, tkesatuan.getText());
                stat.setString(3, tjumlah.getText());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
                datatable();
                bersih();
            }catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan : ID Pangkat sudah ada \n"+e);

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

