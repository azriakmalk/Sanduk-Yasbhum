    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(tcari.getText().equals("")){
            datatable();
        }else{
            Object[] Baris ={"Id Kesatuan","Pangkat","Jumlah Anggota"};
            tabmode = new DefaultTableModel(null,Baris);
            tabel_kesatuan.setModel(tabmode);

            String sql = "select * from Kesatuan where id_kesatuan = '"+tcari.getText()+"' or Kesatuan ='"+tcari.getText()+"'";
            try{
                java.sql.Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                while(hasil.next()){
                    String a = hasil.getString("id_kesatuan");
                    String b = hasil.getString("kesatuan");
                    String c = hasil.getString("jumlah_anggota");
                    String[] data={a,b,c};
                    tabmode.addRow(data);

                }
            }catch (Exception e){

            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

