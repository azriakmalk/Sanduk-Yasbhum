    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        // TODO add your handling code here:
        if(tid.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Id Pangkat Tidak Boleh Kosong","Peringatan",0);
        }else if(tpangkat.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Nama Pangkat Tidak Boleh Kosong","Peringatan",0);
        }else if(tsantunan.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Jumlah Santunan Tidak Boleh Kosong","Peringatan",0);
        }else{
            try{
                String sql = "update pangkat set pangkat=?,jumlah_santunan=? where id_pangkat=?";
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1,tpangkat.getText());
                stat.setString(2,tsantunan.getText());
                stat.setString(3,tid.getText());
                stat.executeUpdate();
                datatable();
                bersih();
                JOptionPane.showMessageDialog(null,"Berhasil Diubah");
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null,"Data Gagal Diubah : "+e);
            }
        }
    }//GEN-LAST:event_beditActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        // TODO add your handling code here:
        if(tid.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Harap Pilih Data Terlebih Dahulu","Peringatan",0);
        }else{
            int ok = JOptionPane.showConfirmDialog(null,"Hapus","Konfirmasi Dialog",JOptionPane.YES_NO_CANCEL_OPTION);
            if(ok==0){
                String sql = "delete from pangkat where id_pangkat='"+tid.getText()+"'";
                try{
                    PreparedStatement stat = conn.prepareStatement(sql);
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null,"Data Berhasil Dihapus");
                    datatable();
                    bersih();
                }catch(SQLException e){
                    JOptionPane.showMessageDialog(null,"Data Gagal Dihapus : "+e);
                }
            }
        }
    }//GEN-LAST:event_bhapusActionPerformed

    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
        // TODO add your handling code here:
        if(tcari.getText().equals("")){
            datatable();
        }else{
            Object[] Baris ={"Id_Pangkat","Pangkat","Jumlah_Santunan"};
            tabmode = new DefaultTableModel(null,Baris);
            tabel_pangkat.setModel(tabmode);

            String sql = "select * from pangkat where id_pangkat = '"+tcari.getText()+"' or pangkat ='"+tcari.getText()+"' or jumlah_santunan ='"+tcari.getText()+"'";
            try{
                java.sql.Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                while(hasil.next()){
                    String a = hasil.getString("id_pangkat");
                    String b = hasil.getString("pangkat");
                    String c = hasil.getString("jumlah_santunan");
                    String[] data={a,b,c};
                    tabmode.addRow(data);

                }
            }catch (Exception e){

            }
        }
    }//GEN-LAST:event_bcariActionPerformed

