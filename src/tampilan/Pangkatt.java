    private void binputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_binputActionPerformed
        // TODO add your handling code here:
        if(tid.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Id Pangkat Tidak Boleh Kosong","Peringatan",0);
        }else if(tpangkat.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Nama Pangkat Tidak Boleh Kosong","Peringatan",0);
        }else if(tsantunan.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Jumlah Santunan Tidak Boleh Kosong","Peringatan",0);
        }else{
            String sql="insert into pangkat values(?,?,?)";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, tid.getText());
                stat.setString(2, tpangkat.getText());
                stat.setString(3, tsantunan.getText());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
                datatable();
                bersih();
            }catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan : ID Pangkat sudah ada \n"+e);

            }
        }
    }//GEN-LAST:event_binputActionPerformed

